"""engine/research/strategy_lifecycle.py — Strategy Lifecycle Manager
(SLM) Phase 0: state machine + transition rules + Pydantic audit-block
schemas.

Doctrine reference: 2026-05-31 deploy-platform RFC ("Strategy Lifecycle
Manager, NOT one-click deploy"). See conversation log + memory entry
[[feedback-promote-deploy-allocate-are-three-gates-2026-05-31]] (write
after Phase 0 ships).

This module provides:

  1. StrategyState enum — the 10 lifecycle states a deployed sleeve
     can be in, from PROPOSED through ARCHIVED.

  2. StateTransition — pure-data record of one transition,
     persisted by strategy_state_store.

  3. TransitionGate — the requirements (evidence) for each allowed
     (from_state, to_state) transition. Used by enforce_transition()
     to reject invalid moves at the type level.

  4. AuditBlocks (Pydantic v2) — typed schema that REPLACES manual
     YAML writing for cost_model + factor_exposure + capacity blocks.
     Validation is enforced at construction; downstream library_writer
     can emit YAML via .to_yaml() with guaranteed schema compliance.

Design principles:
  - Promote != Deploy. Three separate gates (PROMOTE / WIRE / ALLOCATE)
    map to (PROPOSED→AUDITED→APPROVED), (APPROVED→PAPER_TRADE), and
    (PAPER_TRADE→SHADOW→LIVE) respectively. Conflating violates least-
    privilege per the deploy-platform RFC.
  - Every transition records the actor (signature for audit trail).
  - Forward transitions are gated by evidence (Pydantic-validated).
  - Backward transitions (DECAY_WATCH → LIVE) are allowed but require
    explicit override flag — preventing accidental promotion-after-decay.

Pre-Phase-2 limitation: this module defines the gates but does NOT yet
enforce sequential-testing boundaries on PAPER_TRADE → SHADOW. Phase 2
adds the O'Brien-Fleming / Lan-DeMets alpha-spending check.
"""
from __future__ import annotations

