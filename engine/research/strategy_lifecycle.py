class StrategyRecord(BaseModel):
    """Aggregate state for one strategy. Mirrors strategy_state DB row.

    Forward-only fields (proposed_at, audited_at, …) are populated on
    state transitions; backward transitions do NOT clear them so the
    history of "first reached X" is preserved.
    """

    model_config = ConfigDict()

    strategy_id: str = Field(..., min_length=1)
    current_state: StrategyState
    proposed_at: Optional[_dt.datetime] = None
    audited_at: Optional[_dt.datetime] = None
    approved_at: Optional[_dt.datetime] = None
    approved_by: Optional[str] = None
    paper_trade_started: Optional[_dt.datetime] = None
    shadow_started: Optional[_dt.datetime] = None
    live_started: Optional[_dt.datetime] = None
    decommissioned_at: Optional[_dt.datetime] = None
    current_allocation_pct: float = 0.0
    target_allocation_pct: float = 0.0
    library_yaml_path: Optional[str] = None
    candidate_pipeline_run_id: Optional[str] = None
    parent_strategy_id: Optional[str] = None
    notes: str = ""
    created_at: _dt.datetime
    updated_at: _dt.datetime

    @field_validator("current_allocation_pct", "target_allocation_pct")
    @classmethod
    def _validate_alloc(cls, v: float) -> float:
        if v < 0 or v > 1.0:
            raise ValueError("allocation_pct must be in [0, 1]")
        return v
