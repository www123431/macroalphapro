# ── 2. Transition data records ──────────────────────────────────────────


class TransitionGate(BaseModel):
    """Evidence requirements for a single (from, to) transition.

    A gate is a *necessary* (not sufficient) condition. Domain-specific
    callers may impose additional checks beyond what's captured here.
    """

    model_config = ConfigDict(frozen=True)

    requires_candidate_pipeline_run: bool = False
    requires_human_approval: bool = False
    requires_min_paper_trade_months: int = 0
    requires_sequential_test_pass: bool = False
    requires_ramp_protocol_step: Optional[int] = None
    requires_decay_alert_level: Optional[Literal["INFO", "SOFT", "HARD"]] = None
    requires_explicit_override: bool = False
    rationale: str = ""


# ── 3. Allowed transitions table ────────────────────────────────────────
#
# Inspired by:
#   - AQR Beacon strategy lifecycle (public talks 2018-2022)
#   - WorldQuant Alpha Pool auto-decommission framework
#   - Lopez de Prado, "Advances in Financial ML" Ch 10-11
#
# Backward / repair transitions (e.g. DECAY_WATCH → LIVE) intentionally
# require `allows_explicit_override=True` so callers must pass an
# override flag — prevents accidental re-promotion after decay alert.

ALLOWED_TRANSITIONS: dict[tuple[StrategyState, StrategyState], TransitionGate] = {
    # PROPOSED → AUDITED: candidate_pipeline must have run and produced verdict
    (StrategyState.PROPOSED, StrategyState.AUDITED): TransitionGate(
        requires_candidate_pipeline_run=True,
        rationale="candidate_pipeline.PipelineReport required to mark AUDITED",
    ),
    # PROPOSED → REJECTED: candidate_pipeline issued REJECT verdict
    (StrategyState.PROPOSED, StrategyState.REJECTED): TransitionGate(
        requires_candidate_pipeline_run=True,
        rationale="candidate_pipeline verdict REJECT moves to terminal REJECTED",
    ),
    # AUDITED → APPROVED: human signoff (PROMOTE gate)
    (StrategyState.AUDITED, StrategyState.APPROVED): TransitionGate(
        requires_human_approval=True,
        rationale="PROMOTE gate: human signoff required to elevate to APPROVED",
    ),
    # AUDITED → REJECTED: human rejects despite audit pass
    (StrategyState.AUDITED, StrategyState.REJECTED): TransitionGate(
        requires_human_approval=True,
        rationale="human discretion to reject audited strategy (e.g. policy reasons)",
    ),
    # APPROVED → PAPER_TRADE: WIRE gate (auto-deploy script wires sleeve)
    (StrategyState.APPROVED, StrategyState.PAPER_TRADE): TransitionGate(
        rationale="WIRE gate: sleeve registered + paper trade started, no real capital",
    ),
    # PAPER_TRADE → SHADOW: ALLOCATE gate stage 1 (Phase 2 adds seq-test check)
    (StrategyState.PAPER_TRADE, StrategyState.SHADOW): TransitionGate(
        requires_min_paper_trade_months=6,
        requires_sequential_test_pass=True,
        requires_ramp_protocol_step=1,
        rationale="ALLOCATE stage 1: 6mo paper + seq-test pass + ramp to ~1% AUM",
    ),
    # PAPER_TRADE → REJECTED: seq-test early-stop loss boundary hit
    (StrategyState.PAPER_TRADE, StrategyState.REJECTED): TransitionGate(
        requires_sequential_test_pass=False,
        rationale="Sequential testing early-stop loss boundary triggered",
    ),
    # SHADOW → LIVE: ALLOCATE gate stage 2 (ramp through tiers)
    (StrategyState.SHADOW, StrategyState.LIVE): TransitionGate(
        requires_ramp_protocol_step=4,
        rationale="ALLOCATE stage 2: ramped through 1%→5%→15%→target band",
    ),
    # SHADOW → REJECTED: shadow trade Sharpe failed gating
    (StrategyState.SHADOW, StrategyState.REJECTED): TransitionGate(
        rationale="Shadow-trade Sharpe below ramp-gate threshold",
    ),
    # LIVE → DECAY_WATCH: decay sentinel SOFT alert fired
    (StrategyState.LIVE, StrategyState.DECAY_WATCH): TransitionGate(
        requires_decay_alert_level="SOFT",
        rationale="Decay sentinel SOFT alert: trailing Sharpe drift detected",
    ),
    # DECAY_WATCH → LIVE: decay reversed (requires explicit override)
    (StrategyState.DECAY_WATCH, StrategyState.LIVE): TransitionGate(
        requires_explicit_override=True,
        rationale="Decay reversal — requires explicit override to prevent accidental re-promote",
    ),
    # DECAY_WATCH → RAMP_DOWN: HARD alert triggers decommissioning
    (StrategyState.DECAY_WATCH, StrategyState.RAMP_DOWN): TransitionGate(
        requires_decay_alert_level="HARD",
        rationale="Decay sentinel HARD alert: begin auto-decommissioning",
    ),
    # LIVE → RAMP_DOWN: manual decommission (e.g. policy change)
    (StrategyState.LIVE, StrategyState.RAMP_DOWN): TransitionGate(
        requires_human_approval=True,
        rationale="Manual decommission decision (policy / capacity / replacement)",
    ),
    # RAMP_DOWN → ARCHIVED: ramp completed (allocation reached 0)
    (StrategyState.RAMP_DOWN, StrategyState.ARCHIVED): TransitionGate(
        rationale="Ramp-down completed; allocation 0; strategy archived",
    ),
}


def is_terminal(state: StrategyState) -> bool:
    return state in {StrategyState.ARCHIVED, StrategyState.REJECTED}


def allowed_next_states(state: StrategyState) -> list[StrategyState]:
    """All states reachable from `state` in one transition."""
    return [to for (frm, to) in ALLOWED_TRANSITIONS if frm == state]


class InvalidTransitionError(ValueError):
    """Raised when caller attempts a transition not in ALLOWED_TRANSITIONS."""


class GateNotMetError(ValueError):
    """Raised when a transition is allowed in principle but its gate
    evidence is missing or fails validation."""


# ── 3b. Role-specific gates layer ───────────────────────────────────────
#
# Per multi-role doctrine: same (from_state, to_state) transition has
# DIFFERENT acceptance metrics depending on the sleeve's role. Examples:
#
#   alpha_seeker     PAPER_TRADE→SHADOW: trailing Sharpe ≥ P-D8 honest × 0.70
#   risk_premium     PAPER_TRADE→SHADOW: trailing Sharpe ≥ HLZ floor (0.50)
#   insurance        PAPER_TRADE→SHADOW: hedge ratio achieved (NOT Sharpe)
#   regime_overlay   PAPER_TRADE→SHADOW: switching attribution positive
#   diversifier      PAPER_TRADE→SHADOW: cosine ≤ -0.1 (NOT Sharpe)
#
# Phase 0 records the role-specific requirement *structurally*; the
# numerical thresholds are evaluated by Phase 2 sequential testing
# module. The base lifecycle accepts a `role_specific_evidence_passed`
# bool flag that the caller (Phase 2 framework) sets after evaluating
# the role-appropriate metric.


class RoleSpecificGate(BaseModel):
    """Extra requirements for a (role, from→to) tuple beyond the base
    TransitionGate. None means "no role-specific requirement at this
    transition for this role"."""

    model_config = ConfigDict(frozen=True)

    metric_name: str = Field(..., description="human-readable: 'trailing_sharpe', 'hedge_ratio', 'cosine_with_book'")
    threshold_description: str = Field(..., description="natural-language threshold; numeric eval is caller's job")
    rationale: str = ""


ROLE_SPECIFIC_GATES: dict[
    tuple[SleeveRole, StrategyState, StrategyState], RoleSpecificGate
] = {
    # ── alpha_seeker (D_PEAD, PIT SN) ─────────────────────────────────
    (SleeveRole.ALPHA_SEEKER, StrategyState.PAPER_TRADE, StrategyState.SHADOW):
        RoleSpecificGate(
            metric_name="trailing_sharpe",
            threshold_description="trailing 6mo Sharpe ≥ 0.70 × P-D8 honest target",
            rationale="alpha_seeker pays for itself via Sharpe; 70% of honest target is the conservative ramp-in bar",
        ),
    (SleeveRole.ALPHA_SEEKER, StrategyState.SHADOW, StrategyState.LIVE):
        RoleSpecificGate(
            metric_name="trailing_sharpe",
            threshold_description="trailing 12mo Sharpe ≥ 0.80 × P-D8 honest target",
            rationale="full LIVE requires near-target performance demonstrated post-ramp",
        ),
    (SleeveRole.ALPHA_SEEKER, StrategyState.LIVE, StrategyState.DECAY_WATCH):
        RoleSpecificGate(
            metric_name="trailing_sharpe",
            threshold_description="trailing 6mo Sharpe < 0.50 × honest target",
            rationale="alpha_seeker decay signal — Sharpe collapse",
        ),

    # ── risk_premium_harvester (carry, TSMOM) ─────────────────────────
    (SleeveRole.RISK_PREMIUM_HARVESTER, StrategyState.PAPER_TRADE, StrategyState.SHADOW):
        RoleSpecificGate(
            metric_name="trailing_sharpe",
            threshold_description="trailing 6mo Sharpe ≥ 0.50 (HLZ floor)",
            rationale="risk premium harvester — HLZ floor of 0.50 is conservative since the premium IS the alpha source",
        ),
    (SleeveRole.RISK_PREMIUM_HARVESTER, StrategyState.SHADOW, StrategyState.LIVE):
        RoleSpecificGate(
            metric_name="trailing_sharpe",
            threshold_description="trailing 12mo Sharpe ≥ 0.60",
            rationale="full LIVE for premium harvester at modest 0.60 bar",
        ),

    # ── insurance (mom_hedge, crisis_hedge) ───────────────────────────
    # NOTE: Sharpe is INTENTIONALLY NOT the metric. Insurance is bought
    # for negative-correlation hedge effectiveness, not return generation.
    (SleeveRole.INSURANCE, StrategyState.PAPER_TRADE, StrategyState.SHADOW):
        RoleSpecificGate(
            metric_name="hedge_correlation",
            threshold_description="hedge β with risk source ≤ -0.30 sustained over paper-trade window",
            rationale="insurance must demonstrate hedge effectiveness; weak Sharpe is BY DESIGN, not a failure mode",
        ),
    (SleeveRole.INSURANCE, StrategyState.SHADOW, StrategyState.LIVE):
        RoleSpecificGate(
            metric_name="crisis_period_pnl",
            threshold_description="positive PnL in any drawdown period >= 5% in source asset during shadow",
            rationale="insurance LIVE requires actual crisis-protection demonstration",
        ),
    (SleeveRole.INSURANCE, StrategyState.LIVE, StrategyState.DECAY_WATCH):
        RoleSpecificGate(
            metric_name="hedge_correlation",
            threshold_description="rolling 6mo hedge β with risk source > -0.10 (insurance not hedging anymore)",
            rationale="insurance decay = hedge breakdown, NOT Sharpe drop",
        ),

    # ── regime_overlay (vix_oas, multisig switching) ──────────────────
    (SleeveRole.REGIME_OVERLAY, StrategyState.PAPER_TRADE, StrategyState.SHADOW):
        RoleSpecificGate(
            metric_name="switching_attribution",
            threshold_description="positive Sharpe contribution from regime switching vs static baseline",
            rationale="regime overlay must demonstrate IT ADDED value vs holding static weights",
        ),
    (SleeveRole.REGIME_OVERLAY, StrategyState.SHADOW, StrategyState.LIVE):
        RoleSpecificGate(
            metric_name="regime_classifier_accuracy",
            threshold_description="regime classifier hit rate ≥ 60% in shadow",
            rationale="LIVE regime overlay requires classifier to be calibrated, not just lucky",
        ),

    # ── diversifier (TLT/GLD, low-cosine sleeves) ─────────────────────
    (SleeveRole.DIVERSIFIER, StrategyState.PAPER_TRADE, StrategyState.SHADOW):
        RoleSpecificGate(
            metric_name="cosine_with_book",
            threshold_description="cosine with deployed book ≤ -0.10 sustained over paper-trade window",
            rationale="diversifier is bought for negative cosine, NOT Sharpe — Sharpe can be 0 if cosine deeply negative",
        ),
    (SleeveRole.DIVERSIFIER, StrategyState.SHADOW, StrategyState.LIVE):
        RoleSpecificGate(
            metric_name="cosine_with_book",
            threshold_description="cosine remains ≤ -0.10 over shadow window",
            rationale="diversifier LIVE requires cosine to be STABLE — not just momentary",
        ),
    (SleeveRole.DIVERSIFIER, StrategyState.LIVE, StrategyState.DECAY_WATCH):
        RoleSpecificGate(
            metric_name="cosine_with_book",
            threshold_description="rolling 6mo cosine drifted > 0 (correlation flipped, no longer diversifying)",
            rationale="diversifier decay = cosine flip, NOT Sharpe",
        ),
}


def lookup_role_specific_gate(
    role: SleeveRole, from_state: StrategyState, to_state: StrategyState,
) -> Optional[RoleSpecificGate]:
    """Returns the role-specific gate for the given transition, or None
    if no extra requirement applies for this role at this transition."""
    return ROLE_SPECIFIC_GATES.get((role, from_state, to_state))


def enforce_transition(
    *,
    from_state: StrategyState,
    to_state: StrategyState,
    role: Optional[SleeveRole] = None,
    has_candidate_pipeline_run: bool = False,
    has_human_approval: bool = False,
    paper_trade_months: int = 0,
    sequential_test_pass: bool = False,
    role_specific_evidence_passed: bool = False,
    ramp_protocol_step: int = 0,
    decay_alert_level: Optional[Literal["INFO", "SOFT", "HARD"]] = None,
    explicit_override: bool = False,
) -> tuple[TransitionGate, Optional[RoleSpecificGate]]:
    """Validate a transition + its gate evidence. Returns
    (base_gate, role_specific_gate_or_None) on success;
    raises InvalidTransitionError or GateNotMetError on failure.

    Two-layer validation:
      1. Base gate (ALLOWED_TRANSITIONS) — structural eligibility
      2. Role-specific gate (ROLE_SPECIFIC_GATES) — role-conditional
         metric requirement; caller MUST evaluate the metric and pass
         role_specific_evidence_passed=True before this function will
         accept it.

    If role is None, role-specific layer is SKIPPED. Useful for system-
    initiated transitions (state-store internal moves) that shouldn't
    require role context.
    """
    key = (from_state, to_state)
    if key not in ALLOWED_TRANSITIONS:
        raise InvalidTransitionError(
            f"transition {from_state.value} → {to_state.value} is not allowed; "
            f"allowed next states from {from_state.value}: "
            f"{[s.value for s in allowed_next_states(from_state)]}"
        )
    gate = ALLOWED_TRANSITIONS[key]

    # For repair / backward transitions (DECAY_WATCH → LIVE etc.) the
    # gate REQUIRES an explicit override — preventing accidental re-
    # promotion. Override is checked FIRST so non-override callers
    # fail cleanly with a clear error.
    if gate.requires_explicit_override:
        if not explicit_override:
            raise GateNotMetError(
                f"{from_state.value} → {to_state.value} requires explicit_override=True "
                f"(reason: {gate.rationale})"
            )
        return gate, None

    if gate.requires_candidate_pipeline_run and not has_candidate_pipeline_run:
        raise GateNotMetError(
            f"{from_state.value} → {to_state.value} requires "
            f"candidate_pipeline run evidence"
        )
    if gate.requires_human_approval and not has_human_approval:
        raise GateNotMetError(
            f"{from_state.value} → {to_state.value} requires human approval signoff"
        )
    if paper_trade_months < gate.requires_min_paper_trade_months:
        raise GateNotMetError(
            f"{from_state.value} → {to_state.value} requires "
            f"≥{gate.requires_min_paper_trade_months} months paper trade "
            f"(have {paper_trade_months})"
        )
    if gate.requires_sequential_test_pass and not sequential_test_pass:
        raise GateNotMetError(
            f"{from_state.value} → {to_state.value} requires "
            f"sequential-test pass (O'Brien-Fleming / Lan-DeMets)"
        )
    if gate.requires_ramp_protocol_step is not None and \
            ramp_protocol_step < gate.requires_ramp_protocol_step:
        raise GateNotMetError(
            f"{from_state.value} → {to_state.value} requires ramp-protocol "
            f"step ≥{gate.requires_ramp_protocol_step} (have {ramp_protocol_step})"
        )
    if gate.requires_decay_alert_level is not None and \
            decay_alert_level != gate.requires_decay_alert_level:
        raise GateNotMetError(
            f"{from_state.value} → {to_state.value} requires decay alert level "
            f"={gate.requires_decay_alert_level} (have {decay_alert_level})"
        )

    # Role-specific layer: only evaluated if `role` provided
    role_gate: Optional[RoleSpecificGate] = None
    if role is not None:
        role_gate = lookup_role_specific_gate(role, from_state, to_state)
        if role_gate is not None and not role_specific_evidence_passed:
            raise GateNotMetError(
                f"{role.value} role-specific gate at {from_state.value} → "
                f"{to_state.value} NOT MET: metric={role_gate.metric_name}, "
                f"threshold={role_gate.threshold_description!r}"
            )
    return gate, role_gate


# ── 4. Persisted records ────────────────────────────────────────────────


class StateTransitionRecord(BaseModel):
    """Pure-data record persisted to state_transitions table on every
    successful enforce_transition() + state_store.transition() call."""

    model_config = ConfigDict(frozen=True)

    strategy_id: str = Field(..., min_length=1)
    from_state: Optional[StrategyState]
    to_state: StrategyState
    transition_at: _dt.datetime
    actor: str = Field(..., description="signer / script / system that triggered")
    reason: str = ""
    gate_evidence: dict[str, Any] = Field(default_factory=dict)
    git_sha: Optional[str] = None

    @field_validator("transition_at", mode="before")
    @classmethod
    def _coerce_dt(cls, v: Any) -> Any:
        if isinstance(v, str):
            return _dt.datetime.fromisoformat(v.replace("Z", "+00:00"))
        return v


# ── 5. Typed audit blocks (Pydantic v2) ─────────────────────────────────


class MultiAumSharpe(BaseModel):
    model_config = ConfigDict(frozen=True)
    at_10M: float
    at_100M: float
    at_1B: float


class CapacityBlock(BaseModel):
    model_config = ConfigDict(frozen=True)
    hard_capacity_usd: float = Field(..., gt=0)
    binding_constraint: str
    safe_deploy_band_usd: tuple[float, float]
    max_participation_assumed: float = Field(..., gt=0, lt=1)

    @model_validator(mode="after")
    def _validate_band(self) -> "CapacityBlock":
        lo, hi = self.safe_deploy_band_usd
        if lo >= hi:
            raise ValueError("safe_deploy_band_usd must be (lo, hi) with lo < hi")
        return self


class CostModelAudit(BaseModel):
    """Mirrors engine.research.library_cost_model_audit AUDITED_REQUIRED."""

    model_config = ConfigDict(frozen=True)

    audit_status: Literal["audited", "pending"]
    audit_priority: Optional[Literal["high", "medium", "low"]] = None
    audit_date: Optional[_dt.date] = None
    audit_script: Optional[str] = None
    audit_commit: Optional[str] = None
    type: Optional[Literal["almgren_chriss", "scalar_bps_buffer"]] = None
    half_spread_bps: Optional[float] = None
    impact_coef: Optional[float] = None
    daily_sigma_estimate: Optional[float] = None
    universe_median_adv_usd: Optional[float] = None
    n_positions_typical: Optional[int] = None
    monthly_turnover_estimate: Optional[float] = None
    stress_multiplier: Optional[float] = None
    rationale: Optional[str] = None
    multi_aum_sharpe_sleeve: Optional[MultiAumSharpe] = None
    capacity: Optional[CapacityBlock] = None
    caveats: Optional[str] = None

    @model_validator(mode="after")
    def _validate_completeness(self) -> "CostModelAudit":
        if self.audit_status == "audited":
            required = (
                "audit_date", "audit_script", "audit_commit", "type",
                "half_spread_bps", "impact_coef", "daily_sigma_estimate",
                "universe_median_adv_usd", "monthly_turnover_estimate",
                "stress_multiplier", "rationale",
                "multi_aum_sharpe_sleeve", "capacity",
            )
            missing = [k for k in required if getattr(self, k) is None]
            if missing:
                raise ValueError(
                    f"cost_model.audit_status=audited but missing: {missing}"
                )
            if self.rationale and len(self.rationale) < 50:
                raise ValueError("cost_model.rationale must be ≥ 50 chars when audited")
        elif self.audit_status == "pending" and self.audit_priority is None:
            raise ValueError("cost_model.audit_status=pending requires audit_priority")
        return self


